    private void generarMatriz(int n){
        matriz = new int[n][n];
        vectorMayor = new int[n*n];        
        diagonalPrincipal = new int[n];
        String texto = "";
        int pos = 0;
        
        for(int i = 0; i<matriz.length; i++){
            for (int j = 0; j<matriz[i].length; j++){
                // generar numero random de 2 a 4 dig
                matriz[i][j] = ThreadLocalRandom.current().nextInt(10,9999);
                texto = texto + String.format("%6d", matriz[i][j]) + " ";
                
                //Crear vector con elementos de la matriz
                vectorMayor[pos] = matriz[i][j];
                
                //calcular numero mayor
                if (pos == 0){
                    numeroMayor = matriz[i][j];
                    numeroMenor = matriz[i][j];
                }
                
                if (matriz[i][j] > numeroMayor){
                    numeroMayor = matriz[i][j];
                }
                
                if (matriz[i][j] < numeroMenor){
                    numeroMenor = matriz[i][j];
                }
                
                // Ayuda a generar vector de elementos
                pos+=1;
                
                //Llenado de vector diagonal principal
                if (i==j){
                    diagonalPrincipal[i] = matriz[i][j];
                    sumaDiagonalPrincipal += diagonalPrincipal[i];
                    System.out.println(diagonalPrincipal[i]);
                }                                
            }
            texto += "\n";
        }
        
        jMatrizTextArea.setText(texto);
        
        // Calcular vectores mayor a menor y menor a mayor
        texto = "";
        vectorMenor = vectorMayor;
        vectorMenor = Utilitarios.ordenarVector(vectorMenor);
        for (int i = 0; i < vectorMayor.length; i++){
            texto = texto + String.format("%6d", vectorMenor[i]) + " ";
        }
        jVectorMenorMatrizTextArea.setText(texto);
        
        texto = "";
        vectorMayor = vectorMenor;
        vectorMayor = Utilitarios.ordenarVectorMayorAMenor(vectorMayor);
        for (int i = 0; i < vectorMayor.length; i++){
            texto = texto + String.format("%6d", vectorMayor[i]) + " ";
        }
        jVectorMayorMatrizTextArea.setText(texto);
        
        // Numero mayor y menor de la matriz
        jNumeroMayorLabel.setText(numeroMayor+"");
        jNumeroMenorLabel.setText(numeroMenor+"");
        
        //Diagonal principal suma
        jSumaDiagonalPrincipalLabel.setText(sumaDiagonalPrincipal+"");
        
        // suma digitos numeromayor
        int sumaDigitos = 0;
        String aux = Integer.toString(numeroMayor);
        for(int i=0; i<aux.length(); i++){
            sumaDigitos +=  Character.getNumericValue(aux.charAt(i));      
        }
        jSumaNumeroMayorLabel.setText(sumaDigitos+"");
        
    }

