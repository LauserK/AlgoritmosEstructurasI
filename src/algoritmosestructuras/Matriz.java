    private void jGenerarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGenerarButtonActionPerformed
        // TODO add your handling code here:        
        if(jNumeroTextField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debes de ingresar un numero");
        } else {
            tamano = 0;
            try {
                tamano = Integer.parseInt(jNumeroTextField.getText());                
            } catch(Exception e){                                                
            }            
            
            if (tamano <= 0){
                JOptionPane.showMessageDialog(this, "Debes de ingresar un numero valido");                                                
                jNumeroTextField.setText("");
            }
            else {
                jNumeroTextField.setEnabled(false);
                jGenerarButton.setEnabled(false);
                jBorrarButton.setEnabled(true);
                generarMatriz(tamano);
                
            }            
        }
    }//GEN-LAST:event_jGenerarButtonActionPerformed

